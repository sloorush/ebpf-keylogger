def translate_keycode(code, ctrl=0, shift=0, alt=0, meta=0):
    try:
        key = keys[code]
        key = (
            f"{'CTRL+' if ctrl else ''}{'ALT+' if alt else ''}{'SHIFT+' if shift else ''}"
            f"{'META+' if meta else ''}{key}"
        )
        return key
    except KeyError:
        return None
